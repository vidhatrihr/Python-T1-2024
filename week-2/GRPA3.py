word = input().lower()

ans = ''
if 'a' in word:
    ans += 'a'
if 'e' in word:
    ans += 'e'
if 'i' in word:
    ans += 'i'
if 'o' in word:
    ans += 'o'
if 'u' in word:
    ans += 'u'

print(ans)            

